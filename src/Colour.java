/**
 * Enumeration for the different colours that a key or door can have.
 * @author Joseph Vinson
 * @version 1.1
 */
public enum Colour {
    RED, //Represents a red door/key
    GREEN, //Represents a green door/key
    YELLOW, //Represents a yellow door/key
    BLUE, //Represents a blue door/key
    NONE //Represents a door that has been broken open by a boulder
}
